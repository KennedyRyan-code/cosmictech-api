const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const CollaborateSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  projectUrl: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Collaborate = mongoose.model("Collaborate", CollaborateSchema);
module.exports = Collaborate;
